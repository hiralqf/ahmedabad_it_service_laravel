<script>
// Modal logic
let hireModalTimer;
let currentStep = 1;
function showHireModal() {
  document.getElementById('hireDevModal').style.display = 'flex';
  showHireStep(1);
}
function closeHireModal() {
  document.getElementById('hireDevModal').style.display = 'none';
  clearHireModalFields();
}
function showHireStep(step) {
  currentStep = step;
  for (let i = 1; i <= 4; i++) {
    document.getElementById('hireModalStep'+i).style.display = (i === step) ? 'flex' : 'none';
  }
  
  const progressBar = document.getElementById('hireModalProgressBar');
  if (step === 1) progressBar.style.width = '33%';
  else if (step === 2) progressBar.style.width = '66%';
  else if (step === 3) progressBar.style.width = '100%';
  else progressBar.style.width = '100%';
 
  const backIcon = document.getElementById('hireModalBack');
  const closeIcon = document.getElementById('hireModalClose');
  if (step === 2 || step === 3) {
    backIcon.style.display = 'inline';
    closeIcon.style.display = 'none';
  } else {
    backIcon.style.display = 'none';
    closeIcon.style.display = 'inline';
  }
}
function clearHireModalFields() {
  document.getElementById('hireContact').value = '';
  document.getElementById('hireEmail').value = '';
  document.getElementById('hireDuration').value = '';
  document.getElementById('hireContactError').textContent = '';
  document.getElementById('hireEmailError').textContent = '';
  document.getElementById('hireDurationError').textContent = '';
  
  document.querySelectorAll('.hire-modal-btn-alt').forEach(btn => btn.classList.remove('selected'));
}
window.addEventListener('DOMContentLoaded', function() {
  // Only show modal if not previously submitted
  if (!localStorage.getItem('hireDevModalSubmitted')) {
    hireModalTimer = setTimeout(showHireModal, 5000);
  }
  document.getElementById('hireModalClose').onclick = closeHireModal;
  document.getElementById('hireModalDone').onclick = closeHireModal;
  document.getElementById('hireModalBack').onclick = function() {
    if (currentStep === 2) showHireStep(1);
    else if (currentStep === 3) showHireStep(2);
  };
  // Step 1 next
  document.getElementById('hireStep1Next').onclick = function() {
    const phone = document.getElementById('hireContact').value.trim();
    if (!phone.match(/^[0-9]{10}$/)) {
      document.getElementById('hireContactError').textContent = 'Please enter must be 10 digits.';
      return;
    }
    document.getElementById('hireContactError').textContent = '';
    showHireStep(2);
  };
  // Step 2 next
  document.getElementById('hireStep2Next').onclick = function() {
    const email = document.getElementById('hireEmail').value.trim();
    if (!email.match(/^\S+@\S+\.\S+$/)) {
      document.getElementById('hireEmailError').textContent = 'Please enter a valid email address.';
      return;
    }
    document.getElementById('hireEmailError').textContent = '';
    showHireStep(3);
  };
 
  document.querySelectorAll('.hire-modal-btn-alt').forEach(btn => {
    btn.onclick = function(e) {
      e.preventDefault();
    
      document.querySelectorAll('.hire-modal-btn-alt').forEach(b => b.classList.remove('selected'));
      btn.classList.add('selected');
      document.getElementById('hireDuration').value = btn.getAttribute('data-value');
      document.getElementById('hireDurationError').textContent = '';
    };
  });
  // Step 3 submit
  document.getElementById('hireStep3Submit').onclick = function() {
    const duration = document.getElementById('hireDuration').value;
    if (!duration) {
      document.getElementById('hireDurationError').textContent = 'Please select a duration.';
      return;
    }
    document.getElementById('hireDurationError').textContent = '';
    const phone = document.getElementById('hireContact').value.trim();
    const email = document.getElementById('hireEmail').value.trim();

    // Prepare data for AJAX
    const formData = new FormData();
    formData.append('phone', phone);
    formData.append('email', email);
    formData.append('duration', duration);

    fetch('api/enquire.php', {
        method: 'POST',
        body: formData
    })
    .then(response => response.json())
    .then(data => {
        if (data.success) {
            // Set localStorage flag so modal doesn't show again
            localStorage.setItem('hireDevModalSubmitted', '1');
            showHireStep(4);
            clearHireModalFields();
        } else {
            alert(data.message || 'There was an error. Please try again.');
        }
    })
    .catch(error => {
        alert('There was an error. Please try again.');
    });
  };
  // Prevent modal close on click inside
  document.querySelector('.hire-modal-content').onclick = function(e) { e.stopPropagation(); };
  document.getElementById('hireDevModal').onclick = closeHireModal;
});
